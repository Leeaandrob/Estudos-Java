
public class Carro {
	
	public String modelo;
	public double preco; 
	public double consumo_medio;
	public boolean novo; 
	
	public Carro(String modelo, double preco, double consumo_medio, boolean novo){
		this.modelo = modelo;
		this.preco = preco;
		this.consumo_medio = consumo_medio;
		this.novo = novo; 
	}

}
